package com.pdh.player;

public class Player {
	private int id;
	private String name;
	private int holdpower;
	private int jetton;
	private int meoney;
	private String roly;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHoldpower() {
		return holdpower;
	}
	public void setHoldpower(int holdpower) {
		this.holdpower = holdpower;
	}
	public int getJetton() {
		return jetton;
	}
	public void setJetton(int jetton) {
		this.jetton = jetton;
	}
	public int getMeoney() {
		return meoney;
	}
	public void setMeoney(int meoney) {
		this.meoney = meoney;
	}
	public String getRoly() {
		return roly;
	}
	public void setRoly(String roly) {
		this.roly = roly;
	}
}
